                    </a>
                    Desde hace unas semanas, todos los propietarios de tiendas online que tenían contratado un TPV virtual con
                    el Banco Sabadell, La Caixa o directamente con Redsys con algoritmos de cifrado SHA-1 han comenzado
                    a recibir correos electrónicos de aviso con mensajes como
                    <a href="https://canales.redsys.es/" target="_blank">éste</a>
                    sobre la necesidad de actualizar su módulo de TPV.<br><br>
                    El cifrado SHA-1 ha sido declarado obsoleto al no considerarse suficientemente seguro frente a
                    posibles ataques maliciosos y, por tanto, es necesario sustituirlo por algoritmos de cifrado más robustos,
                    como por ejemplo el SHA-256.<br><br>
                    Tenga en cuenta que Redsys podría dejar de admitir operaciones realizadas con módulos inseguros a partir
                    del próximo 20 de Diciembre de 2015, es decir, que los módulos de pago online que no estén actualizados
                    podrían dejar de funcionar.<br><br>
                    Si su empresa tiene contratado un servicio de TPV virtual afectado por este cambio, probablemente haya
                    recibido de Redsys o de su banco documentación informativa y técnica para realizar la adaptación
                    al nuevo sistema.<br><br>
